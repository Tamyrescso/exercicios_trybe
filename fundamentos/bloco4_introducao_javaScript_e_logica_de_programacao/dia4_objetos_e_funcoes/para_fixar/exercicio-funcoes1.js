function soma (a,b) {
    return a + b;
}
console.log('Adição: ' + soma(2,3));

function subtracao (a,b) {
    return a - b;
}
console.log('Subtração: ' + subtracao(10,8));

function multiplicacao (a,b) {
    return a * b;
}
console.log('Multiplicação: ' + multiplicacao(3,4));

function divisao (a,b) {
    return a / b;
}
console.log('Divisão: ' + divisao(8,2));

function modulo (a,b) {
    return a % b;
}
console.log('Módulo: ' + modulo(10,2));

